// Subsistema 2: Sistema de Navegação
class SistemaNavegacao {
    public void ligar() {
        System.out.println("Sistema de navegação ligado");
    }

    public void iniciarRota(String destino) {
        System.out.println("Iniciando rota para: " + destino);
    }

    public void desligar() {
        System.out.println("Sistema de navegação desligado");
    }
}