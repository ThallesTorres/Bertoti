// Subsistema 1: Reprodutor de Música
class ReprodutorMusica {
    public void ligar() {
        System.out.println("Reprodutor de música ligado");
    }

    public void reproduzirMusica(String musica) {
        System.out.println("Reproduzindo música: " + musica);
    }

    public void desligar() {
        System.out.println("Reprodutor de música desligado");
    }
}